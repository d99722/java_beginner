
public class AsciiArtWriter {
	
	public AsciiArtWriter() {
		System.out.println();
	}
	
	public void printBee() {
		System.out.println(" ,-.");
		System.out.println(" \\_/");
		System.out.println(">{|||}-");
		System.out.println(" / \\");
		System.out.println(" '-^ hjw");
		System.out.println();
	}
	
	public void printBee(String name) {
		System.out.println(" ,-.");
		System.out.println(" \\_/");
		System.out.println(">{|||}-"+name+"-");
		System.out.println(" / \\");
		System.out.println(" '-^ hjw");
		System.out.println();
	}
	
	public void printBee(String name1, String name2) {
		System.out.println(" ,-.");
		System.out.println(" \\_/");
		System.out.println(">{|||}-"+name1+"^"+name2+"-");
		System.out.println(" / \\");
		System.out.println(" '-^ hjw");
		System.out.println();
	}
	
	public void printButterfly() {
		System.out.println(" _ \" _");
		System.out.println("(_\\|/_)");
		System.out.println(" (/|\\) ejm97");
		System.out.println();
	}
	
	public void printLadybug() {
		System.out.println(" 'm\'");
		System.out.println(" (|) sahr");
		System.out.println();
	}
	
}
